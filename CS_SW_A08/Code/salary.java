    private void bsearchsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsearchsActionPerformed

        try{
            Class.forName("com.mysql.jdbc.Driver").newInstance();
            con=DriverManager.getConnection("jdbc:mysql://localhost/test?usernae=root & password=");

            st = con.createStatement();
            String n =tsearch.getText();
            rs= st.executeQuery("Select basic_salary,bonus,absence,insurance,tax,total_salary,securty from salary where Emp_id = "+n);
            String bs="";
            String bo="";
            String ab="";
            String in="";
            String tx="";
            String to="";
            String se="";

            while(rs.next()){
                //id=Integer.parseInt(rs.getString("Emp_id"));
                bs=rs.getString("basic_salary");
                bo=rs.getString("bonus");
                ab=rs.getString("absence");
                in=rs.getString("insurance");
                tx=rs.getString("tax");
                to=rs.getString("total_salary");
                se=rs.getString("securty");

            }

            tbasicsalary.setText(bs);
            tbonus.setText(bo);
            tabsence.setText(ab);
            tinsurance.setText(in);
            ttax.setText(tx);
            ttsalary.setText(to);
            tsecurity.setText(se);

        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"catch block");
        }
    }//GEN-LAST:event_bsearchsActionPerformed

