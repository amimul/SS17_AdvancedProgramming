    private void bcalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcalculateActionPerformed

        int bs=Integer.parseInt(tbasicsalary.getText());
        int tx=Integer.parseInt(ttax.getText());
        int in=Integer.parseInt(tinsurance.getText());
        int se=Integer.parseInt(tsecurity.getText());
        int abs=Integer.parseInt(tabsence.getText());
        int bo=Integer.parseInt(tbonus.getText());

        try{
            int t=tx*bs/100;
            int i=in*bs/100;
            int s=se*bs/100;
            int a=abs*bs/30;
            int b=bo*bs/100;
            int all=bs-(t+i+s+a);
            int total=(bo+all);
            ttsalary.setText(total+"");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "catch block");

        }

    }//GEN-LAST:event_bcalculateActionPerformed

